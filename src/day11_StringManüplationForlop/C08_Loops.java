package day11_StringManüplationForlop;

public class C08_Loops {
    public static void main(String[] args) {

        // 10 defa java candır yazdıralım.
        System.out.println("\nJava Candır".repeat(10));

        for (int i=1;i<=10; i++){
            System.out.println("-Java Candır");

        }
        for (char i='a';i<='z'; i++){
            System.out.println(i+"-Java Candır");

        }



    }
}
